@Getter
@NoArgsConstructor
@AllArgsConstructor
public class User implements Serializable {
    private Name name;
    private String gender;
    private Location location;
    private String email;
    private Login login;
    private String phone;
    private Job job;
    private Billing billing;
    private String language;
    private String currency;
}